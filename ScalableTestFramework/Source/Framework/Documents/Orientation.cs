namespace HP.ScalableTest.Framework.Documents
{
    /// <summary>
    /// Page orientation for a document.
    /// </summary>
    public enum Orientation
    {
        /// <summary>
        /// Document pages are in portrait orientation.
        /// </summary>
        Portrait,

        /// <summary>
        /// Document pages are in landscape orientation.
        /// </summary>
        Landscape,

        /// <summary>
        /// Document contains pages with both portrait and landscape orientation.
        /// </summary>
        Mixed
    }
}
